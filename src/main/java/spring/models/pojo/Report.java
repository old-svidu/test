package spring.models.pojo;

public class Report {
    private String name;
    private int countOfficialText;
    private int countEnglishText;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCountOfficialText() {
        return countOfficialText;
    }

    public void setCountOfficialText(int countOfficialText) {
        this.countOfficialText = countOfficialText;
    }

    public int getCountEnglishText() {
        return countEnglishText;
    }

    public void setCountEnglishText(int countEnglishText) {
        this.countEnglishText = countEnglishText;
    }
    }

